def __init__ (self,peso,origem,alergia,tipo): # Construtor
    self.peso = peso
    self.origem = origem
    self.alergia = alergia
    self.tipo = tipo

def __str__ (self): # ToString
    return f'Peso: {self.peso}, Origem: {self.origem}, Alergia: {self.alergia}, Tipo: {self.tipo}'

def get_peso():
    return self.peso

def get_tipo():
    return self.tipo

def get_alergia():
    return self.alergia

def get_origem():
    return self.origem


